package Objects;

public class Report {
    // Fields
    public int spam;
    public int childAbuse;
    public int dangerousOrganization;
    public int scam;
    public int bullying;

    public Report(int spam, int childAbuse, int dangerousOrganization, int scam, int bullying) {
        this.spam = spam;
        this.childAbuse = childAbuse;
        this.dangerousOrganization = dangerousOrganization;
        this.scam = scam;
        this.bullying = bullying;
    }
}

